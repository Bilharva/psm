"""
    4. Triplo dos Valores
Dada a lista valores = [2, 5, 8, 10], imprima o triplo de cada elemento.
Saída esperada:
6
15
24
30
"""
lista = [2, 5, 8, 10]
lista_triplicada = []
print('Lista original: ', lista)
for i in lista:
    lista_triplicada.append(i*3)
    
print('Lista triplicada: ',lista_triplicada)
    

    